describe('the prime numbers canary spec', () => {
  it('shows the infrastructure works', () => {
    true.should.be.true();
  });

  describe('a prime numbers function should', () => {
    it('return none for 1');

    it('return 2 for 2');

    it('return 3 for 3');

    it('return 2, 2 for 4');

    it('return 5 for 5');

    it('return 2, 3 for 6');

    it('return 7 for 7');

    it('return 2, 2, 2 for 8');

    it('return 3, 3 for 9');
  });
});
